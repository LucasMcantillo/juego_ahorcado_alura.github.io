function abrirAgradecimientos() {
  document.getElementById("solapaDiv").style.visibility = "visible";
  document.getElementById("agradecimientosDiv").style.visibility = "visible";
}

function cerrarAgradecimientos() {
  document.getElementById("solapaDiv").style.visibility = "hidden";
  document.getElementById("agradecimientosDiv").style.visibility = "hidden";
}

